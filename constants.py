# constants.py — 암호 시스템 수학 상수
# 이 파일은 공개 수학 상수만 포함 (비밀 키 없음)
# 실제 조합 방법은 Qwen (로컬) 에만 저장

# ─────────────────────────────────
# 리만 제타 영점 (공개 수학값)
# ζ(½ + iρ) = 0 을 만족하는 ρ 값들
# ─────────────────────────────────
RIEMANN_ZEROS = [
    14.134725141734695,
    21.022039638771556,
    25.010857580145689,
    30.424876125859512,
    32.935061587739192,
    37.586178158825671,
    40.918719012147495,
    43.327073280914999,
    48.005150881167159,
    49.773832477672302,
]

# ─────────────────────────────────
# 빛의 속도 (물리 상수)
# ─────────────────────────────────
c = 299_792_458  # m/s

# ─────────────────────────────────
# 삼진법 상태값
# S = +1 → 수소(양성자) = 공개 키
# S = -1 → 산소(전자)  = 개인 키
# S =  0 → 영점(물)    = 암호화 순간, 시간 소멸
# ─────────────────────────────────
S_HYDROGEN =  1   # 수소 +1
S_OXYGEN   = -1   # 산소 -1
S_ZERO     =  0   # 영점 (관측 시 소멸)

# ─────────────────────────────────
# 핵심 수식
# ψ = Σ A_n · e^(i · ρ_n · (x/c - t)) · S
# 영점 조건: x/c = t 일 때 시간 소멸
# ─────────────────────────────────

import cmath

def wave_function(x: float, t: float, S: int) -> complex:
    """
    변형 파동함수
    x: 위치
    t: 시간
    S: 상태 (+1 수소 / -1 산소 / 0 영점)
    """
    if S == S_ZERO:
        return complex(0, 0)  # 영점 — 관측 불가, 시간 소멸

    psi = sum(
        cmath.exp(1j * rho * (x / c - t))
        for rho in RIEMANN_ZEROS
    )
    return psi * S


def is_zero_point(x: float, t: float) -> bool:
    """빛의 속도 조건: x/c = t → 영점, 시간 없음"""
    return abs(x / c - t) < 1e-15
